a = True
b = False

# AND operator (True if both are True)
print("a and b:", a and b)  

# OR operator (True if any is True)
print("a or b:", a or b)    

# NOT operator (reverses the boolean)
print("not a:", not a)       
print("not b:", not b)       

# Using logical operators with comparisons
x = 10
y = 20
z = 5

print("x > y and x > z:", x > y and x > z)  
print("x > y or x > z:", x > y or x > z)    
print("not(x > y):", not(x > y))       

a = 10
b = 3

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division (float)
print("Division:", a / b)

# Floor Division (integer division)
print("Floor Division:", a // b)

# Modulus (remainder)
print("Modulus:", a % b)

# Exponent (power)
print("Exponent:", a ** b)

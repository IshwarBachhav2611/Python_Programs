# 1. Basic print
print("Hello, Python!")

# 2. Print multiple values
name = "Ishwar"
age = 21
print("Name:", name, "Age:", age)

# 3. Print with separator
a = 10
b = 20
c = 30
print(a, b, c, sep="-")

# 4. Print without newline
print("Hello", end=" ")
print("World!")

# 5. Print using f-strings
print(f"My name is {name} and I am {age} years old.")

# 6. Print using string concatenation
print("My name is " + name + " and I am " + str(age) + " years old.")

# 7. Print special characters
print("Hello\nPython")    # Newline
print("Name\tAge")        # Tab
